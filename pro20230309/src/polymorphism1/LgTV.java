package polymorphism1;

public class LgTV {

	public void powerOn()
	{
		System.out.println("LgTV....전원켠다");
	}
	public void powerOff()
	{
		System.out.println("LgTV....전원끈다");
		
	}
	public void volumeUp()
	{

		System.out.println("LgTV....소리 올린다");
	}
	public void volumeOff()
	{

		System.out.println("LgTV....소리 내린다");
	}
}
