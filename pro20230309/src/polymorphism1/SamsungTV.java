package polymorphism1;

public class SamsungTV {
	public void turnOn() {
		System.out.println("SamsungTV....전원켠다 ");
		
	}
	public void turnOff()
	{
		System.out.println("SamsungTV....전원끈다");
	}
	public void soundUp()
	{
		System.out.println("SamsungTV....소리올린다");
	}
	public void soundDown()
	{
		System.out.println("SamsungTV....소리내린다");
	}
}


